function cek(angka) {
    switch (angka) {
        case 1:
            console.log("Angka Satu");
            break;
        case 2:
            console.log("Angka Dua");
            break;
        case 3:
            console.log("Angka Tiga");
            break;
        case 4:
            console.log("Angka Empat");
            break;
        default:
            console.log("Bukan angka 1 - 4");
            break;
    }
}

cek((1))